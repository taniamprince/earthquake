'use strict';

/**
 * @ngdoc overview
 * @name earthquakeApp
 * @description
 * # earthquakeApp
 *
 * Main module of the application.
 */
angular
  .module('earthquakeApp', [
    'ngAnimate',
    'ngCookies',
    'ngResource',
    'ngRoute',
    'ngSanitize',
    'ngTouch'
  ])
  .config(function ($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl',
        controllerAs: 'main'
      })
      .when('/about', {
        templateUrl: 'views/about.html',
        controller: 'AboutCtrl',
        controllerAs: 'about'
      })
      .when('/largest', {
        templateUrl: 'views/largest.html',
        controller: 'LargestCtrl',
        controllerAs: 'largest'
      })
      .when('/search', {
        templateUrl: 'views/search.html',
        controller: 'SearchCtrl',
        controllerAs: 'search'
      })
      .otherwise({
        redirectTo: '/'
      });
  });